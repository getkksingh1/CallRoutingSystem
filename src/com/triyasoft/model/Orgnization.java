package com.triyasoft.model;

public class Orgnization {

	private int org_id;
	private String orgnization_name;
	private String organization_address;

	public int getOrg_id() {
		return org_id;
	}

	public void setOrg_id(int org_id) {
		this.org_id = org_id;
	}

	public String getOrgnization_name() {
		return orgnization_name;
	}

	public void setOrgnization_name(String orgnization_name) {
		this.orgnization_name = orgnization_name;
	}

	public String getOrganization_address() {
		return organization_address;
	}

	public void setOrganization_address(String organization_address) {
		this.organization_address = organization_address;
	}

}
