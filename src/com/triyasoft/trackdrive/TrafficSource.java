package com.triyasoft.trackdrive;

public class TrafficSource {

	private String id;
	private String created_at;
	private String updated_at;
	private String user_updated_at;
	private String user_traffic_source_id;
	private String first_name;
	private String last_name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCreated_at() {
		return created_at;
	}

	public void setCreated_at(String created_at) {
		this.created_at = created_at;
	}

	public String getUpdated_at() {
		return updated_at;
	}

	public void setUpdated_at(String updated_at) {
		this.updated_at = updated_at;
	}

	public String getUser_updated_at() {
		return user_updated_at;
	}

	public void setUser_updated_at(String user_updated_at) {
		this.user_updated_at = user_updated_at;
	}

	public String getUser_traffic_source_id() {
		return user_traffic_source_id;
	}

	public void setUser_traffic_source_id(String user_traffic_source_id) {
		this.user_traffic_source_id = user_traffic_source_id;
	}

	public String getFirst_name() {
		return first_name;
	}

	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}

	public String getLast_name() {
		return last_name;
	}

	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}

}
