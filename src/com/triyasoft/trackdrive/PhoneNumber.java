package com.triyasoft.trackdrive;

public class PhoneNumber {

private String id;
private String number;
private String offer_id;
private String traffic_source_id;
private String provider_configuration_id;
private String created_at;
private String updated_at;


public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getNumber() {
	return number;
}
public void setNumber(String number) {
	this.number = number;
}
public String getOffer_id() {
	return offer_id;
}
public void setOffer_id(String offer_id) {
	this.offer_id = offer_id;
}
public String getTraffic_source_id() {
	return traffic_source_id;
}
public void setTraffic_source_id(String traffic_source_id) {
	this.traffic_source_id = traffic_source_id;
}
public String getProvider_configuration_id() {
	return provider_configuration_id;
}
public void setProvider_configuration_id(String provider_configuration_id) {
	this.provider_configuration_id = provider_configuration_id;
}
public String getCreated_at() {
	return created_at;
}
public void setCreated_at(String created_at) {
	this.created_at = created_at;
}
public String getUpdated_at() {
	return updated_at;
}
public void setUpdated_at(String updated_at) {
	this.updated_at = updated_at;
}



}
