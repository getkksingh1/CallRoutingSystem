public class JsonOptionsWrapper {

	private String Result;
	private Object[] Options ;
	public String getResult() {
		return Result;
	}
	public void setResult(String result) {
		Result = result;
	}
	public Object[] getOptions() {
		return Options;
	}
	public void setOptions(Object[] options) {
		Options = options;
	}
	
	
}
